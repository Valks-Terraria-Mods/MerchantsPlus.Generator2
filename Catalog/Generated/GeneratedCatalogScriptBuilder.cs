using System.Text;

namespace MerchantsPlus.Generator;

internal sealed class GeneratedCatalogScriptBuilder
{
    public string Build(string className, IReadOnlyList<MerchantOutput> merchants)
    {
        var sb = new StringBuilder(capacity: 1_000_000);
        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine();
        sb.AppendLine("namespace MerchantsPlus.Generated;");
        sb.AppendLine();
        sb.AppendLine($"public static class {className}");
        sb.AppendLine("{");
        AppendMerchantsProperty(sb, merchants);
        sb.AppendLine();
        GeneratedCatalogScriptTemplateAppender.AppendIterationMethods(sb);
        sb.AppendLine("}");
        sb.AppendLine();
        GeneratedCatalogScriptTemplateAppender.AppendDataTypes(sb);
        return sb.ToString();
    }

    private static void AppendMerchantsProperty(StringBuilder sb, IReadOnlyList<MerchantOutput> merchants)
    {
        sb.AppendLine("    public static IReadOnlyList<GeneratedMerchant> Merchants { get; } = new GeneratedMerchant[]");
        sb.AppendLine("    {");

        foreach (var merchant in merchants)
            AppendMerchantExpression(sb, merchant, indentLevel: 2, trailingComma: true);

        sb.AppendLine("    };");
    }

    private static void AppendMerchantExpression(StringBuilder sb, MerchantOutput merchant, int indentLevel, bool trailingComma)
    {
        AppendLine(sb, indentLevel, $"new GeneratedMerchant({ToStringLiteral(merchant.Name)},");
        AppendLine(sb, indentLevel + 1, "new GeneratedShop[]");
        AppendLine(sb, indentLevel + 1, "{");

        foreach (var shop in merchant.Shops)
            AppendShopExpression(sb, shop, indentLevel + 2, trailingComma: true);

        AppendLine(sb, indentLevel + 1, $"}}){(trailingComma ? "," : string.Empty)}");
    }

    private static void AppendShopExpression(StringBuilder sb, ShopOutput shop, int indentLevel, bool trailingComma)
    {
        AppendLine(sb, indentLevel, $"new GeneratedShop({ToStringLiteral(shop.Name)},");
        AppendLine(sb, indentLevel + 1, "new GeneratedItem[]");
        AppendLine(sb, indentLevel + 1, "{");

        foreach (var item in shop.Items)
            AppendItemExpression(sb, item, indentLevel + 2);

        AppendLine(sb, indentLevel + 1, $"}}){(trailingComma ? "," : string.Empty)}");
    }

    private static void AppendItemExpression(StringBuilder sb, ItemOutput item, int indentLevel)
    {
        var priceLiteral = ToNullableStringLiteral(item.Price);
        var conditionsLiteral = ToStringArrayLiteral(item.Conditions);
        var isShopSlotLiteral = item.IsShopSlot ? "true" : "false";

        if (item.Shop is null)
        {
            AppendLine(sb, indentLevel, $"new GeneratedItem({ToStringLiteral(item.Name)}, {priceLiteral}, {conditionsLiteral}, {isShopSlotLiteral}, null),");
            return;
        }

        AppendLine(sb, indentLevel, $"new GeneratedItem({ToStringLiteral(item.Name)}, {priceLiteral}, {conditionsLiteral}, {isShopSlotLiteral},");
        AppendShopExpression(sb, item.Shop, indentLevel + 1, trailingComma: false);
        AppendLine(sb, indentLevel, "),");
    }

    private static string ToNullableStringLiteral(string? value)
    {
        return string.IsNullOrWhiteSpace(value) ? "null" : ToStringLiteral(value);
    }

    private static string ToStringArrayLiteral(string[]? values)
    {
        if (values is null || values.Length == 0)
            return "Array.Empty<string>()";

        var literals = new string[values.Length];
        for (var i = 0; i < values.Length; i++)
            literals[i] = ToStringLiteral(values[i]);

        return $"new string[] {{ {string.Join(", ", literals)} }}";
    }

    private static string ToStringLiteral(string value)
    {
        return $"\"{value.Replace("\\", "\\\\").Replace("\"", "\\\"")}\"";
    }

    private static void AppendLine(StringBuilder sb, int indentLevel, string value)
    {
        sb.Append(' ', indentLevel * 4);
        sb.AppendLine(value);
    }
}
